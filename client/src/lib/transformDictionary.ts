const CURSE_PATTERNS = [
  /씨발|시발|씨바|씨빨|ㅅㅂ|ㅅ발|ㅆㅂ/gi,
  /씹새끼|씹놈|씹년|ㅆ새끼/gi,
  /씹(?=같|할|어|고|는|은|지)/gi,
  /개새끼|개색기|개놈|개년|ㄱㅅㄲ|ㄳㄲ/gi,
  /새끼|ㅅㄲ/gi,
  /병신|빙신|ㅂㅅ/gi,
  /지랄|ㅈㄹ/gi,
  /ㅈ같|ㄱ같/gi,
  /개같|개소리|헛소리/gi,
  /존나|존내|ㅈㄴ|겁나/gi,
  /킹받|열받|빡치/gi,
  /미친년|미친놈|미친새끼/gi,
  /미쳤|미치겠|ㅁㅊ/gi,
  /드럽|더럽|ㄷㄹ/gi,
  /뒤져|뒤지/gi,
  /죽어|죽이/gi,
  /때려치|그만두|퇴사/gi,
  /\bf+u+c+k+/gi,
  /\bs+h+i+t+/gi,
  /\bb+i+t+c+h+/gi,
  /\bbastard\b|\basshole\b|\bmother\s*f/gi,
  /\bwtf\b|\bstfu\b|\bgtfo\b|\bffs\b/gi,
  /\bdamn\b|\bcrap\b|\bidiot\b|\bmoron\b/gi,
]

export function curseLevel(raw: string): number {
  return CURSE_PATTERNS.reduce((acc, p) => acc + (raw.match(p)?.length ?? 0), 0)
}

